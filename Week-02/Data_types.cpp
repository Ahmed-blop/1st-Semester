#include <iostream>
using namespace std;
int main()
{
    int a = 10;
    float b = 20.5;
    char c = 'A';
    double d = 30.123456;
    bool e = true;
    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Character: " << c << endl;
    cout << "Double: " << d << endl;
    cout << "Boolean: " << e << endl;
    return 0;

}

